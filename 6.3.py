def langs():
    # Словарь студентов и языков, которые они знают
    students_languages = {
        'Иванов': {'английский', 'французский', 'немецкий'},
        'Петров': {'английский', 'испанский'},
        'Сидоров': {'русский', 'китайский', 'японский'},
        'Козлов': {'китайский', 'французский'},
        'Николаева': {'английский', 'корейский', 'японский'},
        'Кузнецова': {'испанский', 'немецкий'}
    }

    # Множество для всех языков, которые знают студенты
    all_languages = set()
    # Множество для студентов, которые знают китайский язык
    chinese_speakers = set()

    # Перебираем студентов и их языки
    for student, languages in students_languages.items():
        all_languages.update(languages)  # Добавляем языки студента в общее множество языков
        if 'китайский' in languages:
            chinese_speakers.add(student)  # Добавляем студента в множество китайских говорящих

    # Преобразуем множество всех языков в отсортированный список
    sorted_languages = sorted(all_languages)

    # Выводим список всех различных языков, которые знают студенты
    print("Список всех различных языков:")
    for lang in sorted_languages:
        print(lang)

    # Выводим список студентов, которые знают китайский язык
    print("\nСтуденты, которые знают китайский язык:")
    if chinese_speakers:
        for student in chinese_speakers:
            print(student)
    else:
        print("Никто из студентов не знает китайский язык.")

langs()